

public class relationalOperators {
    public static void main(String args[]){
        int a = 10;
        int b = 10;
        //  == operator
       System.out.println(a == b);

    //    != operator
    System.out.println(a != b);

    // > operator
    System.out.println(a > b);

    // < operator
    System.out.println(a < b);

    // >= operator
    System.out.println(a <= b);

    // <= operator
    System.out.println(a <= b);
    }
}
