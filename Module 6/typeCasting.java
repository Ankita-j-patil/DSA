import java.util.*;
public class typeCasting {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        float a = 56.78f;
        int b = (int)a;
        System.out.println(b);

        char ch = 'b';
        int c = ch;
        System.out.println(c);

        sc.close();
    }
}
