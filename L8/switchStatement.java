import java.util.*;
public class switchStatement {
    public static void main(String args[]){
        int number = 7;
        switch(number){
            case 1 : System.out.println("Samosa");
                        break;
            case 2 : System.out.println("Burger");
                        break;
            case 3 : System.out.println("Milkshake");
                        break;
            default : System.out.println("Kuch bhi");
        }
    }
}
